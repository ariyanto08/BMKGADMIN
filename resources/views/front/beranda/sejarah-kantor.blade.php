                        <p class="text-center"><b>Gambar 1. Tampak Depan Stasiun Meteorologi Rahadi Oesman Ketapang</b>
                        </p>
                        <div style="text-align: justify;">
                            <p>Stasiun Meteorologi Rahadi Oesman Ketapang berdiri sejak tahun 1978. Pada tahun 1978
                                stasiun ini masih beroperasi selama 6 jam dimana peralatan yang digunakan untuk
                                pendukung operasional penerbanganpun masih sangat minim. Dari tahun ke tahun peralatan
                                yang digunakan mulai berkembang dan operasional stasiun semakin meningkat. Pada bulan
                                Januari tahun 2013 Stasiun Meteorologi Rahadi Oesman mulai beroperasional selama 24 jam
                                dimana didukung dengan peralatan observasi yang semakin canggih dan SDM yang memadai.
                            </p>
                            <p>Pada tahun 1978-1986 Stasiun Meteorologi Rahadi Oesman dikepalai oleh Bpk R.J.B Megijono
                                (alm) setelah itu dilanjutkan oleh Bpk Sudirman ( 1987-1992). Pada tahun 1992-2009
                                dikepalai oleh Bpk Purwono Sumadi (alm) dilanjutkan dengan Bpk Hazaim, S.Ap dari tahun
                                2009-2015. Selanjutnya pada tahun 2015-2017 dikepalai oleh Bpk Nuralim, S.Si. Pada tahun
                                2017-2020 dikepalai oleh Bpk Aqil Ihsan, S.Kom. Tahun 2020-2022 dikepalai oleh Bpk Heru
                                Sukoco, SE</p>
                            <p>Stasiun Meteorologi Rahadi Oesman merupakan stasiun meteorologi penerbangan yang
                                melakukan pelayanan informasi cuaca yang diperuntukkan untuk keselamatan penerbangan.
                                Selain itu, stasiun meteorologi ini juga melakukan pengamatan sinoptik selama 24 jam
                                yang dipertukarkan secara universal. Pelayanan cuaca publik juga dilakukan kepada
                                masyarakat dengan menyebarkan informasi prakiraan cuaca dan peringatan dini untuk
                                wilayah Ketapang dan Kayong Utara. Dibawah ini merupakan gambar taman alat di Stasiun
                                Meteorologi Rahadi Oesman Ketapang dari tahun 2013-2019.</p>
                        </div>

